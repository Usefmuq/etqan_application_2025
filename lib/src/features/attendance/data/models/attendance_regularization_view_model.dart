import 'package:etqan_application_2025/src/features/attendance/domain/entities/attendance_regularization_view_entity.dart';

class AttendanceRegularizationViewModel
    extends AttendanceRegularizationViewEntity {
  AttendanceRegularizationViewModel({
    required super.id,
    required super.createdById,
    super.createdAt,
    super.updatedAt,
    required super.startDate,
    required super.endDate,
    required super.includeWeekends,
    super.proposedCheckIn,
    super.proposedCheckOut,
    required super.reason,
    super.status,
    required super.requestId,
    required super.isActive,
    super.fullNameEn,
    super.fullNameAr,
    super.email,
    super.phone,
    super.departmentId,
    super.departmentNameEn,
    super.departmentNameAr,
    super.positionId,
    super.positionNameEn,
    super.positionNameAr,
    super.reportTo,
    super.reportToNameEn,
    super.reportToNameAr,
    super.serviceId,
    super.serviceNameEn,
    super.serviceNameAr,
    super.requestStatusId,
    super.requestStatusKey,
    super.requestStatusEn,
    super.requestStatusAr,
    super.priorityId,
    super.priorityKey,
    super.priorityEn,
    super.priorityAr,
    super.requestDetails,
    super.requestCreatedAt,
    super.requestUpdatedAt,
    super.requestApprovedAt,
    required super.requestIsActive,
    super.numberOfManagerApprovalsNeeded,
    super.numberOfApprovalsNeeded,
    required super.numberOfApprovalsDone,
    required super.numberOfApprovalsPending,
  });

  factory AttendanceRegularizationViewModel.fromJson(
      Map<String, dynamic> json) {
    DateTime? dt(dynamic v) =>
        v == null ? null : DateTime.tryParse(v.toString());
    int? parseInt(dynamic v) => v == null ? null : int.tryParse(v.toString());

    return AttendanceRegularizationViewModel(
      id: json['id'] ?? '',
      createdById: json['created_by_id'] ?? '',
      createdAt: dt(json['created_at']),
      updatedAt: dt(json['updated_at']),
      startDate: dt(json['start_date']) ?? DateTime.now(),
      endDate: dt(json['end_date']) ?? DateTime.now(),
      includeWeekends: (json['include_weekends'] ?? false) == true,
      proposedCheckIn: json['proposed_check_in'],
      proposedCheckOut: json['proposed_check_out'],
      reason: json['reason'] ?? '',
      status: json['status'],
      requestId: parseInt(json['request_id']) ?? 0,
      isActive: (json['is_active'] ?? true) == true,
      fullNameEn: json['full_name_en'],
      fullNameAr: json['full_name_ar'],
      email: json['email'],
      phone: json['phone'],
      departmentId: json['department_id'],
      departmentNameEn: json['department_name_en'],
      departmentNameAr: json['department_name_ar'],
      positionId: json['position_id'],
      positionNameEn: json['position_name_en'],
      positionNameAr: json['position_name_ar'],
      reportTo: json['report_to'],
      reportToNameEn: json['report_to_name_en'],
      reportToNameAr: json['report_to_name_ar'],
      serviceId: json['service_id'],
      serviceNameEn: json['service_name_en'],
      serviceNameAr: json['service_name_ar'],
      requestStatusId: json['request_status_id'],
      requestStatusKey: json['request_status_key'],
      requestStatusEn: json['request_status_en'],
      requestStatusAr: json['request_status_ar'],
      priorityId: json['priority_id'],
      priorityKey: json['priority_key'],
      priorityEn: json['priority_en'],
      priorityAr: json['priority_ar'],
      requestDetails: json['request_details'],
      requestCreatedAt: dt(json['request_created_at']),
      requestUpdatedAt: dt(json['request_updated_at']),
      requestApprovedAt: dt(json['request_approved_at']),
      requestIsActive: (json['request_is_active'] ?? true) == true,
      numberOfManagerApprovalsNeeded:
          parseInt(json['number_of_manager_approvals_needed']),
      numberOfApprovalsNeeded: parseInt(json['number_of_approvals_needed']),
      numberOfApprovalsDone: parseInt(json['number_of_approvals_done']) ?? 0,
      numberOfApprovalsPending:
          parseInt(json['number_of_approvals_pending']) ?? 0,
    );
  }

  Map<String, dynamic> toJson() {
    String? ts(DateTime? d) => d?.toUtc().toIso8601String();

    return {
      'id': id,
      'created_by_id': createdById,
      'created_at': ts(createdAt),
      'updated_at': ts(updatedAt),
      'start_date': ts(startDate),
      'end_date': ts(endDate),
      'include_weekends': includeWeekends,
      'proposed_check_in': proposedCheckIn,
      'proposed_check_out': proposedCheckOut,
      'reason': reason,
      'status': status,
      'request_id': requestId,
      'is_active': isActive,
      'full_name_en': fullNameEn,
      'full_name_ar': fullNameAr,
      'email': email,
      'phone': phone,
      'department_id': departmentId,
      'department_name_en': departmentNameEn,
      'department_name_ar': departmentNameAr,
      'position_id': positionId,
      'position_name_en': positionNameEn,
      'position_name_ar': positionNameAr,
      'report_to': reportTo,
      'report_to_name_en': reportToNameEn,
      'report_to_name_ar': reportToNameAr,
      'service_id': serviceId,
      'service_name_en': serviceNameEn,
      'service_name_ar': serviceNameAr,
      'request_status_id': requestStatusId,
      'request_status_key': requestStatusKey,
      'request_status_en': requestStatusEn,
      'request_status_ar': requestStatusAr,
      'priority_id': priorityId,
      'priority_key': priorityKey,
      'priority_en': priorityEn,
      'priority_ar': priorityAr,
      'request_details': requestDetails,
      'request_created_at': ts(requestCreatedAt),
      'request_updated_at': ts(requestUpdatedAt),
      'request_approved_at': ts(requestApprovedAt),
      'request_is_active': requestIsActive,
      'number_of_manager_approvals_needed': numberOfManagerApprovalsNeeded,
      'number_of_approvals_needed': numberOfApprovalsNeeded,
      'number_of_approvals_done': numberOfApprovalsDone,
      'number_of_approvals_pending': numberOfApprovalsPending,
    };
  }
}
