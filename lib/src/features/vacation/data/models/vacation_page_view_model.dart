class VacationsPageViewModel extends VacationsPageView {
  VacationsPageViewModel({
    super.vacationId,
    super.vacationTypeId,
    super.vacationTypeEn,
    super.vacationTypeAr,
    super.reason,
    super.startDate,
    super.endDate,
    super.daysCount,
    super.status,
    super.isActive,
    super.vacationUpdatedAt,
    super.createdById,
    super.fullNameEn,
    super.fullNameAr,
    super.email,
    super.phone,
    super.departmentId,
