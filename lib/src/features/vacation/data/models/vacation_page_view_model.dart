    super.serviceNameEn,
    super.serviceNameAr,
    super.requestStatusId,
    super.requestStatusKey,
    super.requestStatusEn,
    super.requestStatusAr,
    super.priorityId,
    super.priorityKey,
    super.priorityEn,
    super.priorityAr,
    super.requestDetails,
