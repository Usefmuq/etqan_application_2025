  VacationsPageViewModel copyWith({
    String? vacationId,
    String? createdById,
    DateTime? vacationUpdatedAt,
    String? status,
    bool? isActive,
    String? vacationTypeId,
    String? vacationTypeEn,
    String? vacationTypeAr,
    String? reason,
    DateTime? startDate,
    DateTime? endDate,
    double? daysCount,
    String? fullNameEn,
    String? fullNameAr,
    String? email,
    String? phone,
    String? departmentId,
    String? departmentNameEn,
    String? departmentNameAr,
    String? positionId,
    String? positionNameEn,
    String? positionNameAr,
    String? reportTo,
    String? reportToNameEn,
    String? reportToNameAr,
    int? requestId,
    int? serviceId,
    String? serviceNameEn,
    String? serviceNameAr,
    String? requestStatusId,
    String? requestStatusKey,
    String? requestStatusEn,
    String? requestStatusAr,
    String? priorityId,
    String? priorityKey,
    String? priorityEn,
    String? priorityAr,
    String? requestDetails,
    DateTime? requestCreatedAt,
    DateTime? requestUpdatedAt,
    DateTime? requestApprovedAt,
    bool? requestIsActive,
    int? numberOfManagerApprovalsNeeded,
    int? numberOfApprovalsNeeded,
    int? numberOfApprovalsDone,
    int? numberOfApprovalsPending,
  }) {
    return VacationsPageViewModel(
      vacationId: vacationId ?? this.vacationId,
      createdById: createdById ?? this.createdById,
      vacationUpdatedAt: vacationUpdatedAt ?? this.vacationUpdatedAt,
      status: status ?? this.status,
      isActive: isActive ?? this.isActive,
